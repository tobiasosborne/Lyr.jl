const MC_CORNER_OFFSETS = (
    (Int32(0), Int32(0), Int32(0)),  # corner 0
    (Int32(1), Int32(0), Int32(0)),  # corner 1
    (Int32(1), Int32(1), Int32(0)),  # corner 2
    (Int32(0), Int32(1), Int32(0)),  # corner 3
    (Int32(0), Int32(0), Int32(1)),  # corner 4
    (Int32(1), Int32(0), Int32(1)),  # corner 5
