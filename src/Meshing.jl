# =============================================================================
# Main algorithm
# =============================================================================

"""
    volume_to_mesh(grid::Grid{T}; isovalue::T=zero(T)) where {T <: AbstractFloat}
        -> (vertices::Vector{NTuple{3,Float64}}, triangles::Vector{NTuple{3,Int}})

Extract a triangle mesh from a VDB grid using the Marching Cubes algorithm.

Returns world-space vertex positions (multiplied by the grid's voxel size) and
1-indexed triangle vertex indices.

# SDF Convention
Values less than `isovalue` are considered "inside" the surface.

# Example
```julia
grid = create_level_set_sphere(center=(0.0,0.0,0.0), radius=10.0)
vertices, triangles = volume_to_mesh(grid)
```
"""
function volume_to_mesh(grid::Grid{T}; isovalue::T=zero(T)) where {T <: AbstractFloat}
    tree = grid.tree
    vs = _grid_voxel_size(grid)

    # Step 1: Get active bounding box
    bbox = active_bounding_box(tree)
    if bbox === nothing
        return (NTuple{3,Float64}[], NTuple{3,Int}[])
    end

    # Step 2: Expand by 1 voxel on each side to catch boundary cells
    bmin = bbox.min - Coord(Int32(1), Int32(1), Int32(1))
    bmax = bbox.max + Coord(Int32(1), Int32(1), Int32(1))

    # Step 3: Create accessor for cached reads
    acc = ValueAccessor(tree)

    # Output storage
    vertices = NTuple{3,Float64}[]
    triangles = NTuple{3,Int}[]

    # Edge-to-vertex deduplication map
    # Key: sorted pair of corner Coord => vertex index (1-based)
    edge_vertex_map = Dict{Tuple{Coord,Coord}, Int}()

    # Step 4: Iterate each cell in the bounding box
    # Each cell (i,j,k) has 8 corners at (i,j,k) through (i+1,j+1,k+1)
    for k in Int(bmin.z):Int(bmax.z)
        for j in Int(bmin.y):Int(bmax.y)
            for i in Int(bmin.x):Int(bmax.x)
                ci = Int32(i)
                cj = Int32(j)
                ck = Int32(k)

                # Read 8 corner values
                corner_coords = (
                    Coord(ci,     cj,     ck),      # 0
                    Coord(ci + Int32(1), cj,     ck),      # 1
                    Coord(ci + Int32(1), cj + Int32(1), ck),      # 2
                    Coord(ci,     cj + Int32(1), ck),      # 3
                    Coord(ci,     cj,     ck + Int32(1)), # 4
                    Coord(ci + Int32(1), cj,     ck + Int32(1)), # 5
                    Coord(ci + Int32(1), cj + Int32(1), ck + Int32(1)), # 6
                    Coord(ci,     cj + Int32(1), ck + Int32(1)), # 7
                )

                corner_vals = (
                    get_value(acc, corner_coords[1]),
                    get_value(acc, corner_coords[2]),
                    get_value(acc, corner_coords[3]),
                    get_value(acc, corner_coords[4]),
                    get_value(acc, corner_coords[5]),
                    get_value(acc, corner_coords[6]),
                    get_value(acc, corner_coords[7]),
                    get_value(acc, corner_coords[8]),
                )

                # Skip cells at the narrow band boundary (where corners are
                # at background value — these create fake zero-crossings)
                bg = tree.background
                any_bg = (corner_vals[1] == bg || corner_vals[1] == -bg ||
                          corner_vals[2] == bg || corner_vals[2] == -bg ||
                          corner_vals[3] == bg || corner_vals[3] == -bg ||
                          corner_vals[4] == bg || corner_vals[4] == -bg ||
                          corner_vals[5] == bg || corner_vals[5] == -bg ||
                          corner_vals[6] == bg || corner_vals[6] == -bg ||
                          corner_vals[7] == bg || corner_vals[7] == -bg ||
                          corner_vals[8] == bg || corner_vals[8] == -bg)
                any_bg && continue

                # Step 5: Compute cube_index from corner sign tests
                # SDF: val < isovalue => inside => set bit
                cube_index = 0
                if corner_vals[1] < isovalue; cube_index |= 1;   end
                if corner_vals[2] < isovalue; cube_index |= 2;   end
                if corner_vals[3] < isovalue; cube_index |= 4;   end
                if corner_vals[4] < isovalue; cube_index |= 8;   end
                if corner_vals[5] < isovalue; cube_index |= 16;  end
                if corner_vals[6] < isovalue; cube_index |= 32;  end
                if corner_vals[7] < isovalue; cube_index |= 64;  end
                if corner_vals[8] < isovalue; cube_index |= 128; end

                # Skip if entirely inside or outside
                cube_index == 0 && continue
                cube_index == 255 && continue

                # Step 6: Look up edge mask
                edge_bits = MC_EDGE_TABLE[cube_index + 1]

                # Step 7: For each intersected edge, compute or reuse vertex
                # We store up to 12 vertex indices for this cell
                edge_verts = MVector{12, Int}(undef)

                for edge_idx in 0:11
                    if (edge_bits >> edge_idx) & 0x0001 != 0
                        # Get corner indices for this edge
                        ca_idx, cb_idx = MC_EDGE_CORNERS[edge_idx + 1]
                        # +1 for Julia 1-based indexing into corner arrays
                        coord_a = corner_coords[ca_idx + 1]
                        coord_b = corner_coords[cb_idx + 1]
                        val_a = corner_vals[ca_idx + 1]
                        val_b = corner_vals[cb_idx + 1]

                        # Edge key: sorted pair of corner coordinates for dedup
                        edge_key = if (coord_a.x, coord_a.y, coord_a.z) < (coord_b.x, coord_b.y, coord_b.z)
                            (coord_a, coord_b)
                        else
                            (coord_b, coord_a)
                        end

                        vi = get(edge_vertex_map, edge_key, 0)
                        if vi == 0
                            # Interpolate vertex position
                            denom = val_b - val_a
                            t = if abs(denom) < T(1e-12)
                                0.5
                            else
                                Float64(isovalue - val_a) / Float64(denom)
                            end
                            t = clamp(t, 0.0, 1.0)

                            # Position in index space
                            px = Float64(coord_a.x) + t * Float64(coord_b.x - coord_a.x)
                            py = Float64(coord_a.y) + t * Float64(coord_b.y - coord_a.y)
                            pz = Float64(coord_a.z) + t * Float64(coord_b.z - coord_a.z)

                            # Transform to world space
                            wx = px * vs
                            wy = py * vs
                            wz = pz * vs

                            push!(vertices, (wx, wy, wz))
                            vi = length(vertices)
                            edge_vertex_map[edge_key] = vi
                        end

                        edge_verts[edge_idx + 1] = vi
                    end
                end

                # Step 8: Build triangles from TRI_TABLE
                tri_entry = MC_TRI_TABLE[cube_index + 1]
                idx = 1
                while idx <= 16 && tri_entry[idx] >= 0
                    e0 = tri_entry[idx]
                    e1 = tri_entry[idx + 1]
                    e2 = tri_entry[idx + 2]
                    push!(triangles, (edge_verts[e0 + 1], edge_verts[e1 + 1], edge_verts[e2 + 1]))
                    idx += 3
                end
            end
        end
    end

    return (vertices, triangles)
end
